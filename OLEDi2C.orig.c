unsigned char twi_send_start()
{
	TWCR = (1<<TWINT)|(1<<TWSTA)|(1<<TWEN);
	while (!(TWCR & (1<<TWINT)))
		;
	return (TWSR & 0xf8);
	while((TWSR & 0xf8) != 0x08)
		;
}
unsigned char twi_send_repeated_start(void)
{
	TWCR = (1<<TWINT)|(1<<TWSTA)|(1<<TWEN);
	while (!(TWCR & (1<<TWINT)))	;
	return (TWSR & 0xf8);
	while((TWSR & 0xf8) != 0x10)
		;
}

unsigned char twi_send_address_rw(unsigned char address_rw)
{
	TWDR = address_rw;
	TWCR = (1<<TWINT)|(1<<TWEN);
	while (!(TWCR & (1<<TWINT)))
		;
	return (TWSR & 0xf8);
			while(((TWSR & 0xf8) != 0x18) || ((TWSR & 0xf8) != 0x40))
	;
}

unsigned char twi_master_send_data(unsigned char data)
{
	TWDR = data;
	TWCR = (1<<TWINT) |(1<<TWEN);
	while (!(TWCR & (1<<TWINT)))
	;
	return (TWSR & 0xf8);
	while((TWSR & 0xf8) != 0x28)
	;
}

unsigned char twi_master_send_string(unsigned char *string)
{
	while (*string)
	{
		TWDR = *string++;
		TWCR = (1<<TWINT) |(1<<TWEN);
		while (!(TWCR & (1<<TWINT)))
		;
		while((TWSR & 0xf8) != 0x28)
			;
	}
	return (TWSR & 0xf8);
}

